
# "Користувач має ввести своє ім'я, прізвище, улюблений предмет у школі та оцінку за нього.
# Після чого все виводиться на екран одним або кількома рядками (на твій вибір)."

firstName = input("Введіть своє ім'я: ")
secondName = input("Введіть своє прізвище: ")

subject = input("Введіть ваш улюблений предмет: ")
