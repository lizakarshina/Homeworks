# Створіть програму, яка приймає від користувача строку 
# та знаходить кількість голосних літер у цьому рядку Виведіть цю кількість.

str = input('Введіть рядок:\n')

golosni = ['а', 'е', 'у', 'є', 'и', 'і', 'ї', 'о', 'у', 'ю', 'я']
sum = 0

for char in str:
    if char in golosni:
        sum += 1

print(sum)