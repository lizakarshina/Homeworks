# "Запитайте у користувача ввести число N і виведіть всі прості числа, менші або рівні N."
# Вводимо 6, отримуємо 2,3,5
# Просте число це число яке немає дільників окрім себе та одиниці. наприклад 13. Якщо є хоча б один дільник, то число не є простим.

n = int(input('Введіть число: '))

for num in range(1, n + 1):
    is_simple = True 

    for i in range(1, num):
        if num % i == 0:
            is_simple = False  
            break  

    if is_simple:
        print(num)