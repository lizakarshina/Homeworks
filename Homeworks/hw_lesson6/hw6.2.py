# Створити список, елементами якого є інші списки, що містять інформацію про ім’я та прізвище студентів.
# Порахувати скільки людей мають ім’я «Анастасія».

students = [
    ['Анастасія', 'Іваненко'],
    ['Олександр', 'Петренко'],
    ['Анастасія', 'Сидоренко'],
    ['Єлизавета', 'Каршина'],
    ['Анастасія', 'Шевченко']
]

sum = 0

for item in students:
    
    if 'Анастасія' in item:
        sum += 1
        
print(f"Студентів з ім'ям Анастасія: {sum}")